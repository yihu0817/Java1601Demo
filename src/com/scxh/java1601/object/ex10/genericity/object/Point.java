package com.scxh.java1601.object.ex10.genericity.object;

/**
 * 坐标Point
 * 
 * @author zhou.sir
 *
 */
public class Point {
	private Object x = 0; // 经度
	private Object y = 0; // 纬度

	public Object getX() {
		return x;
	}

	public void setX(Object x) {
		this.x = x;
	}

	public Object getY() {
		return y;
	}

	public void setY(Object y) {
		this.y = y;
	}

}
